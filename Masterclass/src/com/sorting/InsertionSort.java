package com.sorting;

public class InsertionSort {

	
	
	public static void main(String[] args) {

		
		int x = 5, y = 5;

		System.out.println(++x); // outputs 6
		System.out.println(x); // outputs 6

		System.out.println(y++); // outputs 5
		System.out.println(y); // outputs 6
		
	}

}
